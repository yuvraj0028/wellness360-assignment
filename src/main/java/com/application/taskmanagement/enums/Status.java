package com.application.taskmanagement.enums;

/**
 * Represents the possible statuses of a task.
 * <ul>
 *     <li>{@code PENDING}: Task is yet to be started.</li>
 *     <li>{@code IN_PROGRESS}: Task is currently being worked on.</li>
 *     <li>{@code COMPLETED}: Task has been finished.</li>
 * </ul>
 */
public enum Status {
    PENDING,
    IN_PROGRESS,
    COMPLETED
}